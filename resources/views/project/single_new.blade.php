ေလ့လာသူေတြက ေကာ္ဖီအတြင္းမွာပါ၀င္တဲ့ ကဖင္းဓါတ္ဟာ ကင္ဆာေရာဂါျပန္လည္ျဖစ္ပြားႏိုင္မႈကို ေလ်ာ့က်ေစတယ္ဆုိတာ ေလ့လာေတြ႔ရွိခဲ့ပါတယ္။

ေကာ္ဖီေသာက္သံုးျခင္းျဖင့္ ေကာင္းမြန္တဲ့ကာကြယ္မႈေတြ သို႔ ရင္သားကင္ဆာ၊ အသည္းကင္ဆာ၊ အေရျပားကင္ဆာ၊ ဆီးက်ိတ္ကင္ဆာစတဲ့ ကင္ဆာေရာဂါေတြကို ကာကြယ္ႏိုင္ၿပီး ဆီးခ်ိဳေရာဂါကိုလည္း ေလ်ာ့နည္းသြားေစေၾကာင္း ေလ့လာေတြ႔ရွိထားၿပီး ျဖစ္ပါတယ္။

 “ ခင္ဗ်ားသာ ေကာ္ဖီေသာက္သံုးသူတစ္ေယာက္ဆိုရင္ အူမႀကီးကင္ဆာအတြက္ ကာကြယ္ၿပီးသားျဖစ္ပါတယ္။ ဆက္ၿပီးေတာ့သာ ေသာက္ပါ” လို႔ Dana Farber ကင္ဆာဌာနရဲ႕ ဒါရိုက္တာျဖစ္သူ Dr. Charles Fuchs ကဆိုပါတယ္။

“ ဒါေပမယ့္ ခင္ဗ်ားက ေကာ္ဖီေသာက္တတ္သူတစ္ဦးမဟုတ္ဘူး စတင္ေသာက္သံုးဖို႔ စဥ္းစားေနရင္တာ့ ကိုယ့္ရဲ႕ဆရာ၀န္နဲ႔အရင္ တိုင္ပင္သင့္ပါတယ္” လို႔လည္း ဆိုပါတယ္။

ေလ့လာသူေတြဟာ အူမႀကီးကင္ဆာ အဆင့္၃ရွိ လူနာ ၁၀၀၀ ကို ေလ့လာခဲ့ရာ တစ္ေန႔ကို ေကာ္ဖီတစ္ခြက္ သို႔ တစ္ခြက္နဲ႔အထက္ - ကဖင္းဓါတ္ ၄၆၀ မီလီဂရမ္ခန္႔ ္ေသာက္သံုးသူေတြဟာ ကင္ဆာေရာဂါျပန္လည္ျဖစ္ပြားဖို႔ ၄၂ရာခိုင္းႏႈန္းခန္႔ ပိုၿပီးသက္သာေစတယ္လို႔ ေတြ႔ရွိခဲ့ပါသတဲ့။ ကင္ဆာေရာဂါေၾကာင့္ ေသဆံုးႏုိင္ေခ် ၃၄ ရာခိုင္ႏႈန္းခန္႔လည္း ေလ်ာ့နည္းသြားေစတယ္လို႔ ဆိုပါတယ္။

Fuchs က ကဖင္းဓါတ္ဟာ ကင္ဆာေရာဂါကိုသာမက ဆီးခ်ိဳေရာဂါအတြက္လည္း အက်ိဳးသက္ေရာက္မႈရွိေစတယ္လို႔ ဆိုပါတယ္။ ေကာ္ဖီဟာ ခႏၶာကိုယ္တြင္း အင္ဆူလင္ သိရွိခံစားႏိုင္မႈကို ျမင့္တက္ေစၿပီး ေယာင္ရမ္းနာက်င္မႈေတြကို သက္သာေစပါတယ္။ ဆီးခ်ိဳေရာဂါကို သက္သာေစရံုသာမက ကင္ဆာေရာဂါပိုမို ဆိုးရြားလာေစမႈကိုလည္း ေလ်ာ့က်ေစပါတယ္။

